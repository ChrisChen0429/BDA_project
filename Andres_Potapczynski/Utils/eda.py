def one_col_summary(data, column, col):
    df = data.groupby(by=column)[col].count()
    df = df.sort_values(ascending=False)
    df = pd.DataFrame(df)
    df['tot'] = df[col].sum()
    df['pct'] = df[col] / df['tot']
    df = df.drop('tot', axis=1)
    return df
